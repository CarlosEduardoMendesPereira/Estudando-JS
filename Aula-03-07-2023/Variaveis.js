nome = "Carlos"; // atribuição do valor a variavel
console.log(nome);

let idade;
idade = 23; // declaração da variavel e atribuição do valor
console.log(idade);

let altura;
altura = 1.78;
console.log(altura);

let cpf = "123.456.789-00";
console.log(cpf);

let casado = false; // false e true
console.log(casado);

// substitui o valor 23 por 24
idade = 24;
console.log(idade);

